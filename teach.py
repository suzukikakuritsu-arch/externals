# ===================================================================
# 鈴木悠起也への説教プログラム
# 外部視点での特徴を踏まえて、やさしく注意喚起する
# ===================================================================

# 特徴リスト
traits = [
    "好奇心旺盛",       # Curiosity
    "細部へのこだわり", # Diligence
    "遊び心",           # Playfulness
    "素直・正直",       # Honesty
    "理論と実務の両立"  # BalanceSense
]

# 注意点メッセージ
advice_messages = [
    "⚠️ 好奇心は素晴らしいけど、時には休息も大事だよ。",
    "⚠️ 細部までこだわるのは良いけど、全体像も忘れずに。",
    "⚠️ 遊び心を忘れずに、でも実務とのバランスを意識しよう。",
    "⚠️ 素直で正直なのは強み、でも他者の意見も取り入れてね。",
    "⚠️ 理論と実務の両立は素晴らしい。無理しすぎず、持続可能に。"
]

# 説教関数
def scold_yukiya(traits, messages):
    print("🔔 鈴木悠起也への説教開始 🔔\n")
    for trait, msg in zip(traits, messages):
        print(f"💡 特徴: {trait}")
        print(f"   {msg}\n")
    print("✅ すべての特徴を活かして、最高の自分を目指してね！")

# 実行
if __name__ == "__main__":
    scold_yukiya(traits, advice_messages)